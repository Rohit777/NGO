        <h3 class="head">Perspective on ELA</h3>
        <p>
        Poverty, social injustice and inequality of opportunities are issues plaguing the marginalized
        sections of the society and are impediments in their development. Girls are placed last in the
        patriarchal order and hence the most neglected and discriminated. We believe that
        education is a powerful tool for their social transformation. It has the potential to challenge
        the patriarchal norms and change the overall view. While education empowers psychologically and 
        socially, livelihood, which is the next step towards development, supplements economically. 
        Emphasis on girl education is based on the view that education imparted to a girl has a ripple 
        effect in the present and to the future generations. As the African proverb goes, “If you educate 
        a man you educate an individual, but if you educate a woman you educate a family”, we concentrate 
        our focus on girls education in the marginalized sections of the society. Furthermore, a capability 
        approach based strategy is required to tackle the issue of livelihood, which is another problem 
        associated with the marginalized sections. Working in close association with the community, they 
        are provided with quality skills that help make a respectable living for themselves. Thus, education
        improves health, incomes and overall livelihood prospects of an individual, while advocacy
        helps spread awareness and attain positive change within the society.
        </p>        
