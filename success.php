            Kanshiram Welfare Foundation with the Adobe Youth Voices Program. AYV, a partnership of Adobe Foundation 
            and American India Foundation, provided a platform for the youth of underprivileged communities to 
            communicate and share their ideas using multimedia and digital tools. His interest lying in photography 
            and video editing, he made four short films during the course of the program on varying issues such as 
            child abuse, child rag pickers and environmental issues. He was highly dedicated and sincere towards his work. 
            After completing his school education, he enrolled in the B. Sc. Program in IGNOU. Presently he has taken up 
            professional photography and has developed a team of his own.
